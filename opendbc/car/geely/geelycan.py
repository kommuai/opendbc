def geely_lkas_0x33_checksum(d: bytearray) -> int:
  """LKAS steering command (CAN id 0x33 only). Not valid for EPS or other frames."""
  return _T2[d[2]] ^ _T3[d[3]] ^ _TC[d[5] >> 4] ^ 0xBF


def geely_checksum(address: int, sig, d: bytearray) -> int:
  """DBC packer hook; must only be registered on BO_ 51 / 0x33 LKAS CHECKSUM."""
  del sig
  if address != LKAS_ADDRESS:
    raise ValueError(f"Geely checksum is only defined for LKAS (0x33), not {address:#x}")
  return geely_lkas_0x33_checksum(d)


# Back-compat alias
geely_lkas_checksum = geely_lkas_0x33_checksum


def create_lkas_command(
  packer, steer_angle_deg, steer_req, meas_angle_deg, steering_deg_sign=1.0, counter=0,
  counter_1bit_1=0, counter_1bit_2=0,
):
  cmd = steering_deg_sign * (steer_angle_deg if steer_req else meas_angle_deg)
  return packer.make_can_msg("LKAS", CANBUS.main_bus, {
    "STEER_CMD": float(cmd),
    "CRUISE_STATE": CRUISE_STATE_ENABLE if steer_req else 0,
    "COUNTER": int(counter) % 16,
    "COUNTER_1BIT_1": int(counter_1bit_1) & 1,
    "COUNTER_1BIT_2": int(counter_1bit_2) & 1,
    "NEW_SIGNAL_1": 0,
    "NEW_SIGNAL_2": 0,
  })
